public class EstruturaDeRepetiçãoFor {
    public static void main(String[] args) {
        for (int minhaCestaItens = 1; minhaCestaItens <= 20; minhaCestaItens ++) {
            System.out.println("Contagens dos itens: " + minhaCestaItens + " xxxxxxxxxxxxxxxx.");
        }
    }
}

